n = input('Type something: ')
print('Type is? {} '.format(type(n)))
print('Is it empty? {}'.format(n.isspace()))
print('Is it a number? {}'.format(n.isnumeric()))
print('Is it a alpha? {}'.format(n.isalpha()))
print('Is it a alphanumeric? {}'.format(n.isalnum()))
print('Is it a uppercase? {}'.format(n.isupper()))
print('Is it a lowercase? {}'.format(n.islower()))
print('Is it a capitalized? {}'.format(n.istitle()))


